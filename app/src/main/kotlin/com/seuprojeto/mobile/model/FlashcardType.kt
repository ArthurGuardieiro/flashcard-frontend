package com.seuprojeto.mobile.model

/**
 * Enum que define os diferentes tipos de flashcards disponíveis no aplicativo.
 */
enum class FlashcardType {
    BASIC, // Frente e verso simples
    CLOZE, // Omissão de palavras no texto
    TYPING, // Digitar a resposta
    MULTIPLE_CHOICE // Questão de múltipla escolha
} 